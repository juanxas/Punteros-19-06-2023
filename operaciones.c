void suma(float *sumando1,float *sumando2, float *suma){
    *suma=*sumando1+*sumando2;
    
}

void resta (float *a,float *b,float *resta){
    *resta=*a-*b;
    
}

void multiplicar (float *c, float *d, float *multiplicar){

    *multiplicar=*c * *d;
    

}

void dividir(float *q, float *p,float *division){
    *division=*q / *p;

}